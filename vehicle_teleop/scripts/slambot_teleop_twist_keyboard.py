	pub = rospy.Publisher('cmd_vel', Twist, queue_size = 1)

	speed = rospy.get_param("~speed", 0.1)
	turn = rospy.get_param("~turn", 10.0 * np.pi/180.0 )
	x = 0
	y = 0
